window.scrollContainer = function scrollContainer(x) {

  if (x) {
    for (let i = 0; i < team.length; i++) {

      if (team[i].active) {
        if (i === 0) {
          scrollMarkers();
          return;
        }
        team[i].active = false;
        team[i - 1].active = true;
        var scrollMeasure = scrollDivs[i - 1].offsetLeft - scrollContainerDiv.offsetLeft;
        scrollContainerDiv.scrollLeft = scrollMeasure;
        scrollMarkers();
        return;
      }
    }
  }
  else {


    for (let i = 0; i < team.length; i++) {
      if (team[i].active) {
        if (i === team.length - 1) {
          return;
        }
        team[i].active = false;
        team[i + 1].active = true;

        var scrollMeasure = scrollDivs[i + 1].offsetLeft - scrollContainerDiv.offsetLeft;
        scrollContainerDiv.scrollLeft = scrollMeasure;
        return;
      }
    }

  }

}

